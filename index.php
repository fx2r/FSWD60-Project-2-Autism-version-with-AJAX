			<h4>Definition Inklusion</h4>
			<p><strong>Der Begriff Inklusion hat seine Wurzeln im Lateinischen. Dort bedeutet das Verb includere einlassen und einschließen, das Sustantiv inclusio bedeutet Einschließung und Einbeziehung.</strong></p>
			<p>Als <strong>soziologischer</strong> Begriff beschreibt das Konzept der Inklusion eine Gesellschaft, in der jeder Mensch akzeptiert wird und gleichberechtigt und selbstbestimmt an dieser teilhaben kann – unabhängig von Geschlecht, Alter oder Herkunft, von Religionszugehörigkeit oder <strong>Bildung</strong>, von eventuellen Behinderungen oder sonstigen individuellen Merkmalen.</p> 
			<p>In der inklusiven Gesellschaft gibt es keine definierte Normalität, die jedes Mitglied dieser Gesellschaft anzustreben oder zu erfüllen hat. Normal ist allein die Tatsache, dass Unterschiede vorhanden sind. Diese Unterschiede werden als Bereicherung aufgefasst und haben keine Auswirkungen auf das selbstverständliche Recht der Individuen auf Teilhabe. Aufgabe der Gesellschaft ist es, in allen Lebensbereichen Strukturen zu schaffen, die es den Mitgliedern dieser Gesellschaft ermöglichen, sich barrierefrei darin zu bewegen.</p> 
			<p>So auch im Bereich der Bildung. Die <strong>inklusive Pädagogik</strong> beschreibt einen Ansatz, der im Wesentlichen auf der Wertschätzung der Vielfalt beruht. In einem inklusiven Bildungssystem lernen Menschen mit und ohne Behinderungen von Anfang an gemeinsam. Homogene und damit separierende Lerngruppen werden nicht gebildet. Von der Kindertagesstätte über die Schulen und Hochschulen bis hin zu Einrichtungen der Weiterbildung wird niemand aufgrund einer Behinderung vom allgemeinen Bildungssystem ausgeschlossen. Vielmehr ist es die Aufgabe des Bildungssystems, durch Bereitstellen von speziellen Mitteln und Methoden einzelne Lernende besonders zu unterstützen und zu fördern. Nicht das Individuum muss sich also an ein bestimmtes System anpassen, sondern das System muss umgekehrt die Bedürfnisse aller Lernenden berücksichtigen und sich gegebenenfalls anpassen. 
			</p>
